"use client";
import { useEffect, useRef } from "react";

type UploadFilesDropdownProps = {
  isOpen: boolean;
  onClose: () => void; // Callback to close the dropdown
};

const UploadFilesDropdown = ({ isOpen, onClose }: UploadFilesDropdownProps) => {
  const dropdownRef = useRef<HTMLDivElement>(null);

  useEffect(() => {
    const handleClickOutside = (event: MouseEvent) => {
      if (
        dropdownRef.current &&
        !dropdownRef.current.contains(event.target as Node)
      ) {
        onClose(); // Close dropdown if clicked outside
      }
    };

    if (isOpen) {
      document.addEventListener("mousedown", handleClickOutside);
    } else {
      document.removeEventListener("mousedown", handleClickOutside);
    }

    return () => {
      document.removeEventListener("mousedown", handleClickOutside);
    };
  }, [isOpen, onClose]);

  return (
    <div
      ref={dropdownRef}
      className={`${
        !isOpen && "hidden"
      } absolute bottom-full bg-white p-1 flex flex-col items-stretch shadow-md rounded-lg border border-slate-300`}
    >
      <div
        className="flex items-center py-2 pl-2 pr-4 text-slate-600 gap-2 hover:bg-slate-100 hover:text-slate-800 rounded-sm"
        onClick={() => {
          console.log("you clicked uploadfiles");
        }}
      >
        <FilesIcon />
        Upload Files
      </div>

      <div
        className="flex items-center py-2 pl-2 pr-4 text-slate-600 gap-2 hover:bg-slate-100 hover:text-slate-800 rounded-sm"
        onClick={() => {
          console.log("you clicked upload images");
        }}
      >
        <ImageIcon />
        Upload Images
      </div>
    </div>
  );
};

export default UploadFilesDropdown;

